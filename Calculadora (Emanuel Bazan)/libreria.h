
/** \brief Funcion para sumar dos numeros.
 * \param recibe primer float.
 * \param recibe segundo float.
 * \return devuelve resultado de la suma como tipo float.
 */
float sumas(float x, float y);

/** \brief Funcion para restar dos numeros.
 * \param recibe primer float.
 * \param recibe segundo float.
 * \return devuelve resultado de la resta como tipo float.
 */
float restas(float x, float y);

/** \brief Funcion para multiplicar dos numeros.
 * \param recibe primer float.
 * \param recibe segundo float.
 * \return devuelve resultado de la multiplicacion como tipo float.
 */
float multiplicaciones(float x, float y);

/** \brief Funcion para dividir dos numeros.
 * \param recibe primer float que sera el dividendo.
 * \param recibe segundo float que sera el divisior.
 * \return devuelve resultado de la division como tipo float.
 */
float divisiones(float x, float y);

/** \brief Funcion para sacar factorial de un numero.
 * \param recibe un dato int del cual calcula factorial.
 * \return devuelve factorial como tipo int.
 */
int factoriales(float x);
